/** 자동완성 검색 — q가 포함된 지역 최대 limit개 반환 */
export function searchRegions(q: string, limit = 8): string[] {
  const query = q.trim();
  if (!query) return [];
  const starts: string[] = [];
  const contains: string[] = [];
  for (const r of REGIONS) {
    // 마지막 토큰(동/리 이름)이 검색어로 시작하면 우선
    const parts = r.split(' ');
    const last = parts[parts.length - 1];
    if (last.startsWith(query)) starts.push(r);
    else if (r.includes(query)) contains.push(r);
    if (starts.length >= limit) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
