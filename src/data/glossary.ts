// 심층 설명(longDef) 병합 — 핵심 용어에 약 1000자 설명 부착
import { LONG_DEFS } from './glossary-long';
for (const t of GLOSSARY) {
  const ld = LONG_DEFS[t.term];
  if (ld) t.longDef = ld;
}

// 가나다 정렬된 전체 목록
export const GLOSSARY_SORTED = [...GLOSSARY].sort((a, b) => a.term.localeCompare(b.term, 'ko'));

// 초성 그룹핑
const CHO = ['ㄱ','ㄲ','ㄴ','ㄷ','ㄸ','ㄹ','ㅁ','ㅂ','ㅃ','ㅅ','ㅆ','ㅇ','ㅈ','ㅉ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'];
const CHO_MERGE: Record<string, string> = { 'ㄲ': 'ㄱ', 'ㄸ': 'ㄷ', 'ㅃ': 'ㅂ', 'ㅆ': 'ㅅ', 'ㅉ': 'ㅈ' };
export function chosung(s: string): string {
  const c = s.charCodeAt(0);
  if (c >= 0xac00 && c <= 0xd7a3) {
    let cho = CHO[Math.floor((c - 0xac00) / 588)];
    return CHO_MERGE[cho] || cho;
  }
  if (/[0-9]/.test(s[0])) return '#';
  return s[0].toUpperCase().replace(/[^A-Z]/, '#');
}

export function searchGlossary(q: string, limit = 30): GTerm[] {
  const s = q.trim().toLowerCase();
  if (!s) return [];
  const starts = GLOSSARY_SORTED.filter(t => t.term.toLowerCase().startsWith(s));
  const inc = GLOSSARY_SORTED.filter(t => !t.term.toLowerCase().startsWith(s) && (t.term.toLowerCase().includes(s) || t.def.toLowerCase().includes(s)));
  return [...starts, ...inc].slice(0, limit);
}

export function relatedTerms(slug: string, limit = 12): GTerm[] {
  return GLOSSARY_SORTED.filter(t => t.rel.includes(slug)).slice(0, limit);
}
